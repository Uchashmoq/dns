static void showBytes(const uint8_t* buf,size_t size){
    for(size_t i=0;i<size;i++){
        printf("%02x ",buf[i]);
        if((i+1)%8 == 0) printf("\t");
        if((i+1)%16 == 0) printf("\n");
    }
    printf("\n");
}

